#include "libft.h"

int		ft_minint(int *ar, int size)
{
	int i;
	int min;

	i = 0;
	min = ar[0];
	while (i < size)
	{
		if (ar[i] < min)
			min = ar[i];
		i++;
	}
	return (min);
}
